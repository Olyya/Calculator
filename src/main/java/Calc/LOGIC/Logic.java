public class Logic {
    static public float addition (float a, float b){
        return a+b;
    }
    static public float extract (float a, float b){
        return a-b;
    }
    static public float multiply (float a, float b){
        return a*b;
    }
    static public float divide (float a, float b){ return a/b; }
}
